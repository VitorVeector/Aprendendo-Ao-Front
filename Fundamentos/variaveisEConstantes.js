/* Criação de uma variável com 'var' */
var x = 3;


/* Também a possivel utilizando o 'let' */
let y = 4;

/* O uso de 'let' é recomendável por suas diferenças, apesar de bastante semelhantes, 'let' é mais moderno e seguro */

// Uma constante não pode ter seu valor alterado.
const z = 5;

// -EXEMPLOS
console.log("'var'");
var v = 2;
console.log(v);;

var v = 30;
console.log(v);

console.log("'let'");
let a = 3;
console.log(a);

a = 34;
console.log(a);

console.log("'const'")
const c = 30;
console.log(c);

const cplus = c + 1;
